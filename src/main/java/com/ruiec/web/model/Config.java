package com.ruiec.web.model;

/**
 * 系统配置模型
 * 
 * @date 2017年11月28日 下午5:57:07
 */
public class Config {
	/** 键 */
	private String key;

	/** 值 */
	private String value;

	/** 键 */
	public String getKey() {
		return key;
	}

	/** 键 */
	public void setKey(String key) {
		this.key = key;
	}

	/** 值 */
	public String getValue() {
		return value;
	}

	/** 值 */
	public void setValue(String value) {
		this.value = value;
	}
}